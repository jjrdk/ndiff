namespace NDiff
{
    /// <summary>
    /// Defines the change action kinds.
    /// </summary>
    public enum ChangeAction
    {
        /// <summary>
        /// Item is unchanged.
        /// </summary>
        Unchanged = 0,

        /// <summary>
        /// Item is added.
        /// </summary>
        Added = 1,

        /// <summary>
        /// Item is removed.
        /// </summary>
        Removed = 2
    }
}
